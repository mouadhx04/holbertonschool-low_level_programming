#include <stdlib.h>
#include <stdio.h>

/**
 * main - print the name of the program
 * @argc: The number of command line arguments in argv array
 * @argv: An array containing the program command line arguments
 * Return: 0 (Success)
 */
int main(__attribute__((unused)) int argc, char *argv[])
{
	printf("%s\n", argv[0]);
	return (0);
}
